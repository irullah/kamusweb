            <h4><b>Tentang Aplikasi Kamus Bahasa Madura-Indonesia Daring</b></h4>
            <p>Aplikasi Kamus Bahasa Madura-Indonesia ini merupakan kamus daring yang basisdatanya mengacu pada MadureseSet [1], yaitu hasil digitalisasi dari Kamus Lengkap Bahasa Madura Indonesia [2] versi cetak. Aplikasi ini dibuat untuk memudahkan pencarian dan contoh penggunaan suatu kata (lema/sub lema) dalam Bahasa Madura sekaligus artinya dalam Bahasa Indonesia. Dengan demikian, aplikasi ini dapat menjadi alat penting bagi pembelajar bahasa, peneliti, penulis, dan siapa pun yang membutuhkan bantuan cepat dan akurat dalam memahami kosakata Bahasa Madura.</p>
            <br>
            <h4><b>Fitur Aplikasi Kamus Bahasa Madura-Indonesia Daring</b></h4>
            <p>Aplikasi ini memiliki daftar fitur berikut:</p>
            <ul>
                <li>Cara menuliskan karakter khusus/beraksen (â, è, ḍ, dan ṭ) yang ada dalam Bahasa Madura</li>
                <li>Pencarian satu kata (aplikasi akan memberikan daftar saran kata yang mungkin dimaksud jika kata yang dicari tidak ada di dalam kamus [3])</li>
                <li>Arti kata (dapat lebih dari satu jika merupakan kata homonim)</li>
                <li>Contoh penggunaan kata</li>
                <li>Keterangan kata (lema/sub lema): kelas kata, dialek, tingkatan bahasa, serapan bahasa asing, serapan bahasa daerah dan lainnya</li>
                <li>Pranala antar kata (lema/sub lema)</li>
            </ul>
